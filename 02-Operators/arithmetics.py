"""
        Arithmetic Operators
    +   addition
    -   substraction
    *   multiplication
    /   division
    //  inteer division
    **  exponentiation
    %   modulus
"""

print(f'20 + 5 = {20 + 5}' )

print(f'20 - 5 = {20 - 5}' )

print(f'20 * 5 = {20 * 5}' )

print(f'20 / 5 = {20 / 5}' )

print(f'20 // 5 = {20 // 5}' )

print(f'20 ^ 5 = {20 ** 2}' )

print(f'20 % 5 = {20 % 5}' )
