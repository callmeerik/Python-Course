"""
        ASSIGNMENT OPERATORS
    =       assign a value to a variable
    +=      x = x + 3
    -=      x = x - 3
    *=      x = x * 3
    /=      x = x / 3
    //=     x = x // 3
    **=     x = x ** 3
    %=      x = x % 3
"""

x = 10
print(f'Original x: {x}')

# addition and assign
x += 9
print(f'x += 9 = {x}' )

# power and assign
x **= 2
print(f'x **= 2 = {x}')

# substraction and assign
x -= 230
print(f'x -= 230 = {x}')