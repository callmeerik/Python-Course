"""
            COMPARISON OPERATORS
    ==          equal to
    !=          different to
    <           lower than
    >           greater than
    <=          lower than or equal to
    >=          greater than or equal to
"""

print(f'19 == 19: {19 == 19}')

print(f'34 < 23: {34 < 23}')

print(f'450 > 319: {450 > 319}')

print(f'989 >= 754: {989 >= 754}')

print(f'1200 >= 988: {1200 >= 988}')