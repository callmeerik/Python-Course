"""
        Strings in Python
"""

course = 'AI with Python'

# print a string
print(f'Length: {len(course)}')

#print whole text
print(f'Course: {course}')

# slicing
print(f'First letter: {course[0]}')

print(f'Last letter: {course[-1]}')

print(f'{course[0: 4]}')

print(f'{course[:3]}')

print(f'{course[::3]}')

numbers = '0123456789'
print(numbers[::2])